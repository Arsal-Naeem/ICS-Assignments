# Prompt user for three numbers and store them in variables num1, num2, and num3.
num1 = int(input("Enter num1: "))
num2 = int(input("Enter num2: "))
num3 = int(input("Enter num3: "))

# Multiply the sum of num1 and num2 by num3 and store the result in a variable called result.
result = (num1 + num2) * num3

# Print the result.
print(result)